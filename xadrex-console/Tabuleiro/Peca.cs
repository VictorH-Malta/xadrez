namespace tabuleiro
{
    abstract class Peca
    {
        public Posicao Posicao { get; set; }
        public Cor Cor { get; protected set; }
        public int QteMovimentos { get; protected set; }
        public Tabuleiro Tab { get; protected set; }

        public Peca(Tabuleiro tab, Cor cor)
        {
            Posicao = null;
            Tab = tab;
            Cor = cor;
            QteMovimentos = 0;
        }


        public void IncrementarQteMovimentos()
        {
            QteMovimentos++;
        }

        public void DecrementarQteMovimentos()
        {
            QteMovimentos--;
        }

        //Criamos um método que retorna uma matriz booleana indicando os possíveis caminhos que uma peça pode executar
        //É um método abstrato porque é muito genérico e depende de cada peça. Logo, sua implementação se dará dentro das classes das peças

        public bool ExisteMovimentosPossiveis()
        {
            //Recebe a matriz de movimentos possíveis
            bool[,] mat = MovimentosPossiveis();
            //Testa se existe ou não espaços na matriz para andar no tabuleiro
            for (int i = 0; i < Tab.Linhas; i++)
            {
                for (int j = 0; j < Tab.Colunas; j++)
                {
                    if (mat[i, j] == true)
                    {
                        return true;
                    }
                }
            }

            return false;
        }

        public bool PodeMoverPara(Posicao pos)
        {
            return MovimentosPossiveis()[pos.Linha, pos.Coluna];
        }

        public abstract bool[,] MovimentosPossiveis();
        

    }
}
