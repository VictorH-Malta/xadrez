using tabuleiro;

namespace xadrez
{   //Classe dedicada à fazer as posições do tabuleiro de xadrez (A1 à H8)
    class PosicaoXadrez
    {
        public char Coluna { get; set; }
        public int Linha { get; set; }

        public PosicaoXadrez(char coluna, int linha)
        {
            Coluna = coluna;
            Linha = linha;
        }

        //Método que converte a posição char+int em uma posição válida na matriz
        public Posicao ToPosicao()
        {
            return new Posicao(8 - Linha, Coluna - 'a');
        }

        public override string ToString()
        {
            return "" + Coluna + Linha;
        }
    }
}
